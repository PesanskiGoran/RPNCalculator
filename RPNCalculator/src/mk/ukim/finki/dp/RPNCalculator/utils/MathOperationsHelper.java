package mk.ukim.finki.dp.RPNCalculator.utils;

/**
 * Class for math operations helper methods.
 */
public class MathOperationsHelper {
    /**
     * Parses string to Double.
     *
     * @param value the value to be parsed
     * @return parsed Double value
     */
    public static Double parseStringToDouble(String value) {
        return Double.parseDouble(value);
    }

    /**
     * Parses string to Integer.
     *
     * @param value the value to be parsed
     * @return parsed Integer value
     */
    public static Integer parseStringToInteger(String value) {
        return Integer.parseInt(value);
    }
}
